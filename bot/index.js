// ARQUITETO: A l√≥gica do concierge foi atualizada para um menu num√©rico,
// incluindo as novas op√ß√µes e a transi√ß√£o para o estado 'HUMANO_ATIVO'.
async function handleConcierge(msg, lowerBody) {
    const choice = parseInt(lowerBody, 10);

    try {
        switch (choice) {
            case 1:
                await msg.reply(`Para ver nosso card√°pio completo e fazer seu pedido, acesse o link:\n\n*${CARDAPIO_URL}*`);
                break;
            case 2:
                const { data: statusData } = await axios.get(`${BACKEND_URL}/api/public/store-status`);
                let addressResponse = `Nosso endere√ßo para retirada √©:\n*${statusData.full_settings.address}*`;
                if (statusData.full_settings.location_link) addressResponse += `\n\nüìç Ver no mapa:\n${statusData.full_settings.location_link}`;
                await msg.reply(addressResponse);
                break;
            case 3:
                const { data: scheduleData } = await axios.get(`${BACKEND_URL}/api/public/store-status`);
                await msg.reply(`*Status atual:* ${scheduleData.status.toUpperCase()}\n\n${scheduleData.message}`);
                break;
            case 4:
                chatStates.set(msg.from, 'HUMANO_ATIVO');
                await msg.reply("Ok, um de nossos atendentes ir√° te responder em instantes. Para reativar o atendimento autom√°tico, digite *reiniciar*.");
                break;
            case 5:
                chatStates.set(msg.from, 'HUMANO_ATIVO');
                await msg.reply("Entendido. J√° notifiquei a nossa equipa. Um respons√°vel entrar√° em contacto em breve. Para reativar o bot, digite *reiniciar*.");
                await sendTelegramNotification(`üîî *Novo Contacto de Fornecedor*\n\nUm poss√≠vel fornecedor/parceiro entrou em contacto no WhatsApp.\n\n*Contacto:* ${msg.from.replace('@c.us', '')}\n\nPor favor, verifique a conversa.`);
                break;
            case 6:
                chatStates.set(msg.from, 'HUMANO_ATIVO');
                await msg.reply("Ol√°, parceiro! Nossa equipa de log√≠stica j√° foi notificada e ir√° responder em breve. Se quiser, pode adiantar sua d√∫vida. Para reativar o bot, digite *reiniciar*.");
                await sendTelegramNotification(`üèçÔ∏è *Novo Contacto de Entregador*\n\nUm entregador/parceiro entrou em contacto no WhatsApp.\n\n*Contacto:* ${msg.from.replace('@c.us', '')}\n\nPor favor, verifique a conversa.`);
                break;
            default:
                // Se n√£o for um n√∫mero v√°lido, verifica por palavras-chave de produto/bebida
                if (DRINK_KEYWORDS.some(kw => lowerBody.includes(kw))) {
                    await msg.reply("Ol√°! No momento, focamos em oferecer as melhores pamonhas e derivados, por isso n√£o trabalhamos com bebidas. üòä");
                } else if (PRODUCT_KEYWORDS.some(kw => lowerBody.includes(kw))) {
                    const cleanQuery = cleanSearchQuery(lowerBody);
                    const { data } = await axios.get(`${BACKEND_URL}/api/public/product-query`, { params: { q: cleanQuery } });
                    if(data.encontrado) {
                        await msg.reply(data.emEstoque ? `Temos *${data.nome}* sim! üòä\n\nPode pedir em nosso card√°pio:\n*${CARDAPIO_URL}*` : `Poxa, nosso(a) *${data.nome}* esgotou! üò•\n\nVeja outras del√≠cias em:\n*${CARDAPIO_URL}*`);
                    } else {
                        await sendDefaultMenu(msg);
                    }
                } else {
                    await sendDefaultMenu(msg);
                }
                break;
        }
    } catch (error) {
        log('ERROR', 'Concierge', `Falha ao processar mensagem: ${error.message}`);
        await msg.reply("Desculpe, tive um problema. Tente novamente ou digite *4* para falar com um atendente.");
    }
}


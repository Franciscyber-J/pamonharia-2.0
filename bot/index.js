async function sendTelegramNotification(message) {
    if (!TELEGRAM_BOT_TOKEN || !TELEGRAM_CHAT_ID) {
        log('WARN', 'Telegram', 'Token ou Chat ID do Telegram n√£o configurados. A saltar notifica√ß√£o.');
        return;
    }
    try {
        const url = `https://api.telegram.org/bot${TELEGRAM_BOT_TOKEN}/sendMessage`;
        await axios.post(url, {
            chat_id: TELEGRAM_CHAT_ID,
            text: message,
            parse_mode: 'Markdown'
        });
        log('SUCCESS', 'Telegram', 'Notifica√ß√£o enviada com sucesso para o Telegram.');
    } catch (error) {
        const errorMessage = error.response ? JSON.stringify(error.response.data) : error.message;
        log('ERROR', 'Telegram', `Falha ao enviar notifica√ß√£o para o Telegram: ${errorMessage}`);
    }
}
